package code;

public class Abstract {

//	abstract means hiding implementation.

//	1. if class having an unimplemented method then the method should be declared as abstract.
//	2. if class having at least one abstract method then the class should be declared as abstract.
//	3. it may or may not have implementation methods.
//	4. a class can be declared as abstract even it doesn't have abstract method.
//	5. we cannot create an object for abstract class.
//	6. if a child doesn't implement all abstract methods of parent class,
//	then the child class must need to be declared abstract as well.
}
